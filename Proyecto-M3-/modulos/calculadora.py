
def sumar(a, b):
    return a + b # suma de dos numeros 

def restar(a, b):
    return a - b #resta de dos numeros

def multiplicar(a, b): #multiplicacion de dos numeros
    return a * b

def dividir(a, b): #division de dos numeros
    if b == 0:
        return "Error: no se puede dividir entre cero"
    return a / b
